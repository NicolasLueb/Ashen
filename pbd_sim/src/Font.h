class BitmapFont
{
public:
    void init()
    {
        buildShader();
        glGenVertexArrays(1,&m_vao);
        glGenBuffers(1,&m_vbo);
        glBindVertexArray(m_vao);
        glBindBuffer(GL_ARRAY_BUFFER,m_vbo);
        glEnableVertexAttribArray(0);
        glVertexAttribPointer(0,2,GL_FLOAT,GL_FALSE,2*sizeof(float),(void*)0);
        glBindVertexArray(0);
    }

    ~BitmapFont()
    {
        glDeleteVertexArrays(1,&m_vao);
        glDeleteBuffers(1,&m_vbo);
        glDeleteProgram(m_shader);
    }

    // Measure text width in pixels at given scale
    float measureWidth(const std::string& text, float scale) const
    {
        return text.size() * (5+1) * scale;
    }

    // Draw text at screen pixel position (top-left origin)
    void drawText(const std::string& text,
                  float x, float y, float scale,
                  float r, float g, float b, float a,
                  const glm::mat4& proj)
    {
        glUseProgram(m_shader);
        glUniformMatrix4fv(glGetUniformLocation(m_shader,"uProj"),
                           1,GL_FALSE,glm::value_ptr(proj));
        glUniform4f(glGetUniformLocation(m_shader,"uColor"),r,g,b,a);
        glEnable(GL_BLEND);
        glBlendFunc(GL_SRC_ALPHA,GL_ONE_MINUS_SRC_ALPHA);

        float cx = x;
        for (char ch : text)
        {
            drawChar(ch, cx, y, scale);
            cx += (5+1) * scale;
        }
    }

    // Draw centered text
    void drawTextCentered(const std::string& text,
                          float cx, float y, float scale,
                          float r, float g, float b, float a,
                          const glm::mat4& proj)
    {
        float w = measureWidth(text, scale);
        drawText(text, cx - w*0.5f, y, scale, r,g,b,a, proj);
    }

    // Draw a number
    void drawNumber(int n, float x, float y, float scale,
                    float r, float g, float b, float a,
                    const glm::mat4& proj)
    {
        drawText(std::to_string(n), x, y, scale, r,g,b,a, proj);
    }

    // Draw text with a dark shadow for readability
    void drawTextShadow(const std::string& text,
                        float x, float y, float scale,
                        float r, float g, float b, float a,
                        const glm::mat4& proj)
    {
        // Shadow pass
        drawText(text, x+scale, y+scale, scale, 0.f,0.f,0.f,a*0.6f, proj);
        // Main pass
        drawText(text, x, y, scale, r,g,b,a, proj);
    }

    void drawTextShadowCentered(const std::string& text,
                                 float cx, float y, float scale,
                                 float r, float g, float b, float a,
                                 const glm::mat4& proj)
    {
        float w = measureWidth(text, scale);
        drawTextShadow(text, cx-w*0.5f, y, scale, r,g,b,a, proj);
    }

private:
    void drawChar(char ch, float x, float y, float scale)
    {
        int idx = (int)ch - 32;
        if (idx < 0 || idx >= (int)(sizeof(kFontData)/sizeof(kFontData[0]))) return;

        std::vector<float> verts;
        verts.reserve(7*5*12);

        for (int row = 0; row < 7; ++row)
        {
            uint8_t bits = kFontData[idx][row];
            for (int col = 0; col < 5; ++col)
            {
                if (!(bits & (0x10 >> col))) continue;
                float px = x + col * scale;
                float py = y + row * scale;
                float s  = scale;
                // two triangles
                verts.insert(verts.end(),{
                    px,   py,   px+s, py,   px+s, py+s,
                    px,   py,   px+s, py+s, px,   py+s,
                });
            }
        }

        if (verts.empty()) return;
        glBindVertexArray(m_vao);
        glBindBuffer(GL_ARRAY_BUFFER,m_vbo);
        glBufferData(GL_ARRAY_BUFFER,
                     verts.size()*sizeof(float),
                     verts.data(), GL_DYNAMIC_DRAW);
        glDrawArrays(GL_TRIANGLES,0,(GLsizei)(verts.size()/2));
        glBindVertexArray(0);
    }

    void buildShader()
    {
        const char* vs = R"glsl(
#version 330 core
layout(location=0) in vec2 aPos;
uniform mat4 uProj;
void main(){ gl_Position = uProj * vec4(aPos, 0.0, 1.0); }
)glsl";
        const char* fs = R"glsl(
#version 330 core
out vec4 O;
uniform vec4 uColor;
void main(){ O = uColor; }
)glsl";
        auto cs=[](GLenum t,const char* s)->GLuint{
            GLuint id=glCreateShader(t);
            glShaderSource(id,1,&s,nullptr);glCompileShader(id);
            return id;
        };
        GLuint v=cs(GL_VERTEX_SHADER,vs),f=cs(GL_FRAGMENT_SHADER,fs);
        m_shader=glCreateProgram();
        glAttachShader(m_shader,v);glAttachShader(m_shader,f);
        glLinkProgram(m_shader);
        glDeleteShader(v);glDeleteShader(f);
    }

    GLuint m_shader=0, m_vao=0, m_vbo=0;
};
